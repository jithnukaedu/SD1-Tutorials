#Addition (+)
result = 5 + 3
print(result)  # Output: 8

#Subtraction (-)
result = 10 - 4
print(result)  # Output: 6

#Multiplication (*)
result = 3 * 5
print(result)  # Output: 15

#Division (/)
result = 10 / 2
print(result)  # Output: 5.0

#Modulus (%)
result = 10 % 3
print(result)  # Output: 1 (remainder of 10 divided by 3)

#Exponentiation (**):
result = 2 ** 3
print(result)  # Output: 8 (2 raised to the power of 3)

#Floor Division (//):
result = 10 // 3
print(result)  # Output: 3 (integer division of 10 by 3)
